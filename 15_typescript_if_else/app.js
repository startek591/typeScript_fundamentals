// TypeScript if statement
var max = 100;
var counter = 0;
if (counter < max) {
    counter++;
}
console.log(counter);
var max2 = 100;
var counter2 = 100;
if (counter2 < max2) {
    counter2++;
}
console.log(counter2);
// TypeScript if-else statement
var max3 = 100;
var counter3 = 100;
if (counter3 < max3) {
    counter3++;
}
else {
    counter3 = 1;
}
console.log(counter3);
// Ternary operator?:
var max4 = 100;
var counter4 = 100;
counter4 < max4 ? counter4++ : (counter = 1);
console.log(counter4);
